SELECT DISTINCT c.CODCARRERA, a.TANTOPORUNO FROM CARRERAS c, APUESTAS a
WHERE c.CODCARRERA = a.CODCARRERA AND a.TANTOPORUNO>3;

/*2*/
SELECT NOMBRE, APELLIDOS FROM PERSONAS
WHERE FECHA_NACIMIENTO>'31-12-1979' ORDER BY FECHA_NACIMIENTO DESC;

/*3*/
SELECT DISTINCT CODCABALLO FROM PARTICIPACIONES
WHERE POSICIONFINAL IN (1,2,3) ORDER BY CODCABALLO;

/*4*/
SELECT * FROM CARRERAS WHERE IMPORTEPREMIO > 50000;

/*5*/
SELECT NOMBRE, DNI FROM CLIENTES
WHERE NACIONALIDAD != 'ESCOCESA' AND NACIONALIDAD != 'BRITANICA';

/*6*/
SELECT C.PROPIETARIO, C.NOMBRE FROM CABALLOS C, PERSONAS P
WHERE P.CODIGO = C.PROPIETARIO AND
(C.FECHANACIMIENTO =TO_DATE('11','MM') OR C.PESO>260)
ORDER BY P.CODIGO DESC, C.NOMBRE ASC;

/*7*/
SELECT DISTINCT NOMBRE FROM PERSONAS
WHERE NOMBRE LIKE UPPER('J%') AND NOMBRE LIKE LOWER('%E%');

