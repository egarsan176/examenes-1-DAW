    SELECT P.CODCABALLO FROM PARTICIPACIONES P
    WHERE P.POSICIONFINAL = 1
    GROUP BY P.CODCABALLO
    HAVING COUNT(*) > 1;

/*9*/
SELECT MAX(IMPORTEPREMIO) FROM CARRERAS;

/*10*/
SELECT MAX(a.IMPORTE) FROM APUESTAS a, CLIENTES c
WHERE C.DNI = A.DNICLIENTE;

/*11*/
SELECT c.NOMBRE, a.IMPORTE AS DINERO_GANADO FROM CLIENTES c, APUESTAS a
WHERE c.DNI = a.DNICLIENTE;

/*12*/
SELECT P.NOMBRE, P.APELLIDOS, C.NOMBRE AS NOMBRE_CABALLO FROM PERSONAS P, PARTICIPACIONES PA, CABALLOS C
WHERE P.CODIGO = PA.JOCKEY AND PA.CODCABALLO = C.CODCABALLO AND PA.POSICIONFINAL = 1;

/*13 */
SELECT DISTINCT P.NOMBRE, P.APELLIDOS FROM PERSONAS P, PARTICIPACIONES PA
WHERE P.CODIGO = PA.JOCKEY;

/*14*/
SELECT C.NOMBRECARRERA, CL.NOMBRE, A.IMPORTE FROM CARRERAS C, CLIENTES CL, APUESTAS A
WHERE C.CODCARRERA = A.CODCARRERA AND CL.DNI = A.DNICLIENTE AND A.IMPORTE > C.APUESTALIMITE;

/*15 */
SELECT P.NOMBRE, P.APELLIDOS, C.NOMBRE AS NOMBRE_CABALLO FROM PERSONAS P, CABALLOS C
WHERE P.CODIGO = C.PROPIETARIO GROUP BY P.NOMBRE, P.APELLIDOS, C.NOMBRE HAVING COUNT(*) > 2;

/*16*/
SELECT DISTINCT C.NOMBRE, C.NACIONALIDAD FROM CLIENTES C, APUESTAS A
WHERE C.DNI = A.DNICLIENTE AND A.IMPORTE > 800;



