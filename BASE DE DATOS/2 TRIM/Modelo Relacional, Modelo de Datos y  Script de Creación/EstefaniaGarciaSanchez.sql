    SELECT DISTINCT C.CODCABALLO, SUM(P.POSICIONFINAL) FROM CABALLOS C, PARTICIPACIONES P
    WHERE C.CODCABALLO = P.CODCABALLO AND P.POSICIONFINAL = 1 GROUP BY C.CODCABALLO;

